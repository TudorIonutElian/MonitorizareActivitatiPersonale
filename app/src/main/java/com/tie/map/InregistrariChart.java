package com.tie.map;

public class InregistrariChart {
    private int id;
    private int kg;

    public InregistrariChart(int id, int kg) {
        this.id = id;
        this.kg = kg;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getKg() {
        return kg;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }
}
